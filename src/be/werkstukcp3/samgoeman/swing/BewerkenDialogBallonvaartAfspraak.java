    public void setTeBewerkenBallonvaartAfspraakID(int opstijgterreinID) {
        BallonvaartAfspraak teBewerkenBallonvaartAfspraak = BallonvaartAfspraakDao.getBallonvaartAfspraakById(opstijgterreinID);

        this.txtBallonvaartAfspraakID.setText(Integer.toString(teBewerkenBallonvaartAfspraak.getBallonvaartAfspraakID()));
        this.txtDatum.setText(teBewerkenBallonvaartAfspraak.getDatum());
        this.txtAantalPassagiers.setText(Integer.toString(teBewerkenBallonvaartAfspraak.getAantalPassagiers()));

        updateBallonnen();
        updateOpstijgterreinen();
        updateCrewleden();
        updateKlanten();
        updatePiloten();

        int selectedBallonID = teBewerkenBallonvaartAfspraak.getBallonID();
        Ballon geselecteerdeBallon = BallonDao.getBallonById(selectedBallonID);
        cbxBallon.setSelectedItem(geselecteerdeBallon);
        
        int selectedOpstijgterreinID = teBewerkenBallonvaartAfspraak.getOpstijgterreinID();
        Opstijgterrein geselecteerdOpstijgterrein = OpstijgterreinDao.getOpstijgterreinById(selectedOpstijgterreinID);
        cbxOpstijgterrein.setSelectedItem(geselecteerdOpstijgterrein);
        
        lstKlant.setSelectedIndex(0);
        lstPiloot.setSelectedIndex(0);
        lstCrewlid.setSelectedIndex(0);
       

    }

    public BallonvaartAfspraak getIngevuldBallonvaartAfspraak() {
        Ballon geselecteerdeBallon = (Ballon) cbxBallon.getSelectedItem();
        Opstijgterrein geselecteerdOpstijgterrein = (Opstijgterrein) cbxOpstijgterrein.getSelectedItem();

        return new BallonvaartAfspraak(Integer.parseInt(this.txtBallonvaartAfspraakID.getText()), this.txtDatum.getText(), Integer.parseInt(this.txtAantalPassagiers.getText()), geselecteerdOpstijgterrein.getOpstijgterreinID(), geselecteerdeBallon.getBallonID());
    }

