    public void updateOpstijgterreinen() {
        mijnOpstijgterreinen.removeAllElements();
        ArrayList<Opstijgterrein> lijstVanOpstijgterreinen = OpstijgterreinDao.getOpstijgterreinen();
        for (Opstijgterrein huidigOpstijgterrein : lijstVanOpstijgterreinen) {
            mijnOpstijgterreinen.addElement(huidigOpstijgterrein);
        }
    }

    private void updateBallonnen() {
        mijnBallonnen.removeAllElements();
        ArrayList<Ballon> lijstVanBallonnen = BallonDao.getBallonnen();
        for (Ballon huidigBallon : lijstVanBallonnen) {
            mijnBallonnen.addElement(huidigBallon);
        }
    }

    public void updateKlanten() {
        mijnKlanten.clear();
        ArrayList<Klant> lijstVanKlanten = KlantDao.getKlanten();
        for (Klant huidigKlant : lijstVanKlanten) {
            mijnKlanten.addElement(huidigKlant);
        }
    }

    public void updatePiloten() {
        mijnPiloten.clear();
        ArrayList<Piloot> lijstVanPiloten = PilootDao.getPiloten();
        for (Piloot huidigPiloot : lijstVanPiloten) {
            mijnPiloten.addElement(huidigPiloot);
        }
    }

    public void updateCrewleden() {
        mijnCrewleden.clear();
        ArrayList<Crewlid> lijstVanCrewleden = CrewlidDao.getCrewleden();
        for (Crewlid huidigCrewlid : lijstVanCrewleden) {
            mijnCrewleden.addElement(huidigCrewlid);
        }
    }
