    private void lstPilootValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstPilootValueChanged
        if (lstPiloot.isSelectionEmpty()) {
            btnVerwijderen.setEnabled(false);
            btnWijzigen.setEnabled(false);
            btnDetails.setEnabled(false);
        } else {
            btnVerwijderen.setEnabled(true);
            btnWijzigen.setEnabled(true);
            btnDetails.setEnabled(true);
        }
    }//GEN-LAST:event_lstPilootValueChanged

    private void btnToevoegenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnToevoegenActionPerformed
        ToevoegenDialogPiloot toevoegDialogPiloot = new ToevoegenDialogPiloot(this, true);

        ToevoegenDialogPiloot.Resultaat mijnResultaatToevoegenPiloot = toevoegDialogPiloot.showDialog();

        if (mijnResultaatToevoegenPiloot == ToevoegenDialogPiloot.Resultaat.TOEGEVOEGD) {
            Piloot opgehaaldPiloot = toevoegDialogPiloot.getIngevuldPiloot();
            PilootDao.voegPilootToe(opgehaaldPiloot);
            updatePilotenLijst();
        }

    }//GEN-LAST:event_btnToevoegenActionPerformed

    private void btnWijzigenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWijzigenActionPerformed
        Piloot teWijzigenPiloot = (Piloot) lstPiloot.getSelectedValue();

        BewerkenDialogPiloot bewerkDialogPiloot = new BewerkenDialogPiloot(this, true);

        bewerkDialogPiloot.setTeBewerkenPilootID(teWijzigenPiloot.getPilootID());

        BewerkenDialogPiloot.Resultaat mijnResultaatBewerkenPiloot = bewerkDialogPiloot.showDialog();

        if (mijnResultaatBewerkenPiloot == BewerkenDialogPiloot.Resultaat.TOEGEVOEGD) {
            Piloot opgehaaldPiloot = bewerkDialogPiloot.getIngevuldPiloot();
            PilootDao.updatePiloot(opgehaaldPiloot);
            updatePilotenLijst();
        }
    }//GEN-LAST:event_btnWijzigenActionPerformed

    private void btnVerwijderenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerwijderenActionPerformed
        Piloot geselecteerdPiloot = (Piloot) lstPiloot.getSelectedValue();
        ArrayList<PilootAfspraak> mijnPilootAfspraken = PilootAfspraakDao.getPilootAfsprakenByPilootId(geselecteerdPiloot.getPilootID());

        if (!mijnPilootAfspraken.isEmpty()) {
            int resultaat = JOptionPane.showConfirmDialog(null, "Er hangen afspraken aan deze piloot, weet u zeker dat u deze piloot wilt verwijderen?", "Opgepast!", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (resultaat == JOptionPane.YES_OPTION) {
                PilootAfspraakDao.verwijderPilootAfspraakByPilootId(geselecteerdPiloot.getPilootID());
                PilootDao.verwijderPiloot(geselecteerdPiloot.getPilootID());
                PilootDao.resetAutoIncrement(geselecteerdPiloot.getPilootID());
            }
        } else {
            int resultaat = JOptionPane.showConfirmDialog(null, "Weet u zeker dat u deze piloot wilt verwijderen?", "Opgepast!", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (resultaat == JOptionPane.YES_OPTION) {
                PilootDao.verwijderPiloot(geselecteerdPiloot.getPilootID());
                PilootDao.resetAutoIncrement(geselecteerdPiloot.getPilootID());
            }
        }
        updatePilotenLijst();
    }//GEN-LAST:event_btnVerwijderenActionPerformed

    private void txtInputKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtInputKeyReleased
        String input = "%" + txtInput.getText() + "%";
        ArrayList<Piloot> lijstVanPiloten = PilootDao.searchPiloot(input);
        mijnModel.clear();
        if (!lijstVanPiloten.isEmpty()) {
            for (Piloot huidigPiloot : lijstVanPiloten) {
                mijnModel.addElement(huidigPiloot);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Geen piloten gevonden");
        }
    }//GEN-LAST:event_txtInputKeyReleased

