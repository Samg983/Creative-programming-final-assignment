    public void updatePilotenLijst() {
        mijnModel.clear();
        ArrayList<Piloot> lijstVanPilooten = PilootDao.getPiloten();
        for (Piloot huidigPiloot : lijstVanPilooten) {
            mijnModel.addElement(huidigPiloot);
        }
    }

