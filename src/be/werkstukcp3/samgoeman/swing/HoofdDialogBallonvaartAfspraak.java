    public void updateBallonvaartAfsprakenLijst() {
        mijnModel.clear();
        ArrayList<BallonvaartAfspraak> lijstVanBallonvaartAfspraaken = BallonvaartAfspraakDao.getBallonvaartAfspraken();
        for (BallonvaartAfspraak huidigBallonvaartAfspraak : lijstVanBallonvaartAfspraaken) {
            mijnModel.addElement(huidigBallonvaartAfspraak);
        }
    }

