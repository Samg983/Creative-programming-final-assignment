    private void btnToevoegenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnToevoegenActionPerformed
        ToevoegenDialogBallonvaartAfspraak toevoegDialogBallonvaartAfspraak = new ToevoegenDialogBallonvaartAfspraak(this, true);

        ToevoegenDialogBallonvaartAfspraak.Resultaat mijnResultaatToevoegenBallonvaartAfspraak = toevoegDialogBallonvaartAfspraak.showDialog();

        if (mijnResultaatToevoegenBallonvaartAfspraak == ToevoegenDialogBallonvaartAfspraak.Resultaat.TOEGEVOEGD) {

            BallonvaartAfspraak opgehaaldBallonvaartAfspraak = toevoegDialogBallonvaartAfspraak.getIngevuldBallonvaartAfspraak();
            BallonvaartAfspraakDao.voegBallonvaartAfspraakToe(opgehaaldBallonvaartAfspraak);

            //id ophalen van laatst toegevoegde BallonvaartAfspraak
            ArrayList getLijst = BallonvaartAfspraakDao.getBallonvaartAfspraken();
            BallonvaartAfspraak lastBallonvaartAfspraak = (BallonvaartAfspraak) getLijst.get(getLijst.size() - 1);

            //Crewlid afspraken toevoegen
            List<Crewlid> mijnOpgehaaldeCrewleden = toevoegDialogBallonvaartAfspraak.getIngevuldCrewAfspraak();

            for (Crewlid crewlid : mijnOpgehaaldeCrewleden) {
                CrewAfspraak nieuwCrewAfspraak = new CrewAfspraak(0, lastBallonvaartAfspraak.getBallonvaartAfspraakID(), crewlid.getCrewlidID());
                CrewAfspraakDao.voegCrewAfspraakToe(nieuwCrewAfspraak);
            }

            //Klanten afspraken toevoegen
            List<Klant> mijnOpgehaaldeKlanten = toevoegDialogBallonvaartAfspraak.getIngevuldKlantAfspraak();

            for (Klant klant : mijnOpgehaaldeKlanten) {
                KlantAfspraak nieuweKlantAfspraak = new KlantAfspraak(0, lastBallonvaartAfspraak.getBallonvaartAfspraakID(), klant.getKlantID());
                KlantAfspraakDao.voegKlantAfspraakToe(nieuweKlantAfspraak);
            }

            //Piloot afspraken toevoegen
            List<Piloot> mijnOpgehaaldePiloten = toevoegDialogBallonvaartAfspraak.getIngevuldPilootAfspraak();

            for (Piloot piloot : mijnOpgehaaldePiloten) {
                PilootAfspraak nieuwePilootAfspraak = new PilootAfspraak(0, lastBallonvaartAfspraak.getBallonvaartAfspraakID(), piloot.getPilootID());
                PilootAfspraakDao.voegPilootAfspraakToe(nieuwePilootAfspraak);
            }

            updateBallonvaartAfsprakenLijst();

        }

    }//GEN-LAST:event_btnToevoegenActionPerformed

    private void btnWijzigenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWijzigenActionPerformed
        BallonvaartAfspraak teWijzigenBallonvaartAfspraak = (BallonvaartAfspraak) lstBallonvaartAfspraak.getSelectedValue();

        BewerkenDialogBallonvaartAfspraak bewerkDialogBallonvaartAfspraak = new BewerkenDialogBallonvaartAfspraak(this, true);

        bewerkDialogBallonvaartAfspraak.setTeBewerkenBallonvaartAfspraakID(teWijzigenBallonvaartAfspraak.getBallonvaartAfspraakID());

        BewerkenDialogBallonvaartAfspraak.Resultaat mijnResultaatBewerkenBallonvaartAfspraak = bewerkDialogBallonvaartAfspraak.showDialog();

        if (mijnResultaatBewerkenBallonvaartAfspraak == BewerkenDialogBallonvaartAfspraak.Resultaat.TOEGEVOEGD) {
            BallonvaartAfspraak opgehaaldBallonvaartAfspraak = bewerkDialogBallonvaartAfspraak.getIngevuldBallonvaartAfspraak();
            BallonvaartAfspraakDao.updateBallonvaartAfspraak(opgehaaldBallonvaartAfspraak);

            int opgehaaldeBallonvaartAfspraakID = opgehaaldBallonvaartAfspraak.getBallonvaartAfspraakID();

            //Crewlid afspraken updaten
            List<Crewlid> mijnOpgehaaldeCrewleden = bewerkDialogBallonvaartAfspraak.getIngevuldCrewAfspraak();

            //eerst allemaal verwijderen, daarna opnieuw invoeren
            CrewAfspraakDao.verwijderCrewAfspraakByBallonvaartAfspraakId(opgehaaldeBallonvaartAfspraakID);

            for (Crewlid crewlid : mijnOpgehaaldeCrewleden) {
                CrewAfspraak nieuwCrewAfspraak = new CrewAfspraak(0, opgehaaldeBallonvaartAfspraakID, crewlid.getCrewlidID());
                CrewAfspraakDao.voegCrewAfspraakToe(nieuwCrewAfspraak);
            }

            //Klanten afspraken updaten
            KlantAfspraakDao.verwijderKlantAfspraakByBallonvaartAfspraakId(opgehaaldeBallonvaartAfspraakID);

            List<Klant> mijnOpgehaaldeKlanten = bewerkDialogBallonvaartAfspraak.getIngevuldKlantAfspraak();

            for (Klant klant : mijnOpgehaaldeKlanten) {
                KlantAfspraak nieuweKlantAfspraak = new KlantAfspraak(0, opgehaaldeBallonvaartAfspraakID, klant.getKlantID());
                KlantAfspraakDao.voegKlantAfspraakToe(nieuweKlantAfspraak);
            }

            //Piloot afspraken udpaten
            PilootAfspraakDao.verwijderPilootAfspraakByBallonvaartAfspraakId(opgehaaldeBallonvaartAfspraakID);

            List<Piloot> mijnOpgehaaldePiloten = bewerkDialogBallonvaartAfspraak.getIngevuldPilootAfspraak();

            for (Piloot piloot : mijnOpgehaaldePiloten) {
                PilootAfspraak nieuwePilootAfspraak = new PilootAfspraak(0, opgehaaldeBallonvaartAfspraakID, piloot.getPilootID());
                PilootAfspraakDao.voegPilootAfspraakToe(nieuwePilootAfspraak);
            }

        }

        updateBallonvaartAfsprakenLijst();
    }//GEN-LAST:event_btnWijzigenActionPerformed

    private void btnVerwijderenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerwijderenActionPerformed
        BallonvaartAfspraak geselecteerdBallonvaartAfspraak = (BallonvaartAfspraak) lstBallonvaartAfspraak.getSelectedValue();

        int geselecteerdBallonvaartAfspraakID = geselecteerdBallonvaartAfspraak.getBallonvaartAfspraakID();
        int resultaat = JOptionPane.showConfirmDialog(null, "Weet u zeker dat u deze ballonvaart afspraak wilt verwijderen?", "Opgepast!", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (resultaat == JOptionPane.YES_OPTION) {
            CrewAfspraakDao.verwijderCrewAfspraakByBallonvaartAfspraakId(geselecteerdBallonvaartAfspraakID);
            KlantAfspraakDao.verwijderKlantAfspraakByBallonvaartAfspraakId(geselecteerdBallonvaartAfspraakID);
            PilootAfspraakDao.verwijderPilootAfspraakByBallonvaartAfspraakId(geselecteerdBallonvaartAfspraakID);
            BallonvaartAfspraakDao.verwijderBallonvaartAfspraak(geselecteerdBallonvaartAfspraakID);

            CrewAfspraakDao.resetAutoIncrement(geselecteerdBallonvaartAfspraakID);
            KlantAfspraakDao.resetAutoIncrement(geselecteerdBallonvaartAfspraakID);
            PilootAfspraakDao.resetAutoIncrement(geselecteerdBallonvaartAfspraakID);
            BallonvaartAfspraakDao.resetAutoIncrement(geselecteerdBallonvaartAfspraakID);
        }
        updateBallonvaartAfsprakenLijst();
    }//GEN-LAST:event_btnVerwijderenActionPerformed

