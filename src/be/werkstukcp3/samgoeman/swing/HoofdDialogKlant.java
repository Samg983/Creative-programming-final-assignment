    public void updateKlantenLijst() {
        mijnModel.clear();
        ArrayList<Klant> lijstVanKlanten = KlantDao.getKlanten();
        for (Klant huidigKlant : lijstVanKlanten) {
            mijnModel.addElement(huidigKlant);
        }
    }

