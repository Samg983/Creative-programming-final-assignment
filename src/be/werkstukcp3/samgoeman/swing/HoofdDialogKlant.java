    private void lstKlantValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstKlantValueChanged
        if (lstKlant.isSelectionEmpty()) {
            btnVerwijderen.setEnabled(false);
            btnWijzigen.setEnabled(false);
            btnDetails.setEnabled(false);
        } else {
            btnVerwijderen.setEnabled(true);
            btnWijzigen.setEnabled(true);
            btnDetails.setEnabled(true);
        }
    }//GEN-LAST:event_lstKlantValueChanged

    private void btnToevoegenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnToevoegenActionPerformed
        ToevoegenDialogKlant toevoegDialogKlant = new ToevoegenDialogKlant(this, true);

        ToevoegenDialogKlant.Resultaat mijnResultaatToevoegenKlant = toevoegDialogKlant.showDialog();

        if (mijnResultaatToevoegenKlant == ToevoegenDialogKlant.Resultaat.TOEGEVOEGD) {
            Klant opgehaaldKlant = toevoegDialogKlant.getIngevuldKlant();
            KlantDao.voegKlantToe(opgehaaldKlant);
            updateKlantenLijst();
        }

    }//GEN-LAST:event_btnToevoegenActionPerformed

    private void btnWijzigenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWijzigenActionPerformed
        Klant teWijzigenKlant = (Klant) lstKlant.getSelectedValue();

        BewerkenDialogKlant bewerkDialogKlant = new BewerkenDialogKlant(this, true);

        bewerkDialogKlant.setTeBewerkenKlantID(teWijzigenKlant.getKlantID());

        BewerkenDialogKlant.Resultaat mijnResultaatBewerkenKlant = bewerkDialogKlant.showDialog();

        if (mijnResultaatBewerkenKlant == BewerkenDialogKlant.Resultaat.TOEGEVOEGD) {
            Klant opgehaaldKlant = bewerkDialogKlant.getIngevuldKlant();
            KlantDao.updateKlant(opgehaaldKlant);
            updateKlantenLijst();
        }
    }//GEN-LAST:event_btnWijzigenActionPerformed

    private void btnVerwijderenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerwijderenActionPerformed
        Klant geselecteerdKlant = (Klant) lstKlant.getSelectedValue();
        ArrayList<KlantAfspraak> mijnKlantenAfspraken = KlantAfspraakDao.getKlantAfsprakenByKlantId(geselecteerdKlant.getKlantID());

        if (!mijnKlantenAfspraken.isEmpty()) {
            int resultaat = JOptionPane.showConfirmDialog(null, "Er hangen afspraken aan deze klant, weet u zeker dat u deze klant wilt verwijderen?", "Opgepast!", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (resultaat == JOptionPane.YES_OPTION) {
                KlantAfspraakDao.verwijderKlantAfspraakByKlantId(geselecteerdKlant.getKlantID());
                KlantDao.verwijderKlant(geselecteerdKlant.getKlantID());
                KlantDao.resetAutoIncrement(geselecteerdKlant.getKlantID());
            }
        } else {
            int resultaat = JOptionPane.showConfirmDialog(null, "Weet u zeker dat u deze klant wilt verwijderen?", "Opgepast!", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (resultaat == JOptionPane.YES_OPTION) {
                KlantDao.verwijderKlant(geselecteerdKlant.getKlantID());
                KlantDao.resetAutoIncrement(geselecteerdKlant.getKlantID());

            }
        }
        updateKlantenLijst();
    }//GEN-LAST:event_btnVerwijderenActionPerformed

    private void txtInputKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtInputKeyReleased
        String input = "%" + txtInput.getText() + "%";
        ArrayList<Klant> lijstVanKlanten = KlantDao.searchKlant(input);
        mijnModel.clear();
        if (!lijstVanKlanten.isEmpty()) {
            for (Klant huidigKlant : lijstVanKlanten) {
                mijnModel.addElement(huidigKlant);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Geen klanten gevonden");
        }
    }//GEN-LAST:event_txtInputKeyReleased

